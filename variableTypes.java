    System.out.println (num2);
    //short -32,768 to 32,767
    short num3 = -31956;
    short num4 = 32555;
    System.out.println (num3);
    System.out.println (num4);
    //int -2,147,483,468 to 2,147,483,647
    int num5 = -2122888765;
